package entities;

public class Porcentagem {

	private double porcentagemHorista, porcentagemIntegral, porcentagemDoutor, porcentagemMestre,
			porcentagemEspecialista;

	public double getPorcentagemHorista() {
		return porcentagemHorista;
	}

	public void setPorcentagemHorista(double porcentagemHorista) {
		this.porcentagemHorista = porcentagemHorista;
	}

	public double getPorcentagemIntegral() {
		return porcentagemIntegral;
	}

	public void setPorcentagemIntegral(double porcetagemIntegral) {
		this.porcentagemIntegral = porcetagemIntegral;
	}

	public double getPorcentagemDoutor() {
		return porcentagemDoutor;
	}

	public void setPorcentagemDoutor(double porcentagemDoutor) {
		this.porcentagemDoutor = porcentagemDoutor;
	}

	public double getPorcentagemMestre() {
		return porcentagemMestre;
	}

	public void setPorcentagemMestre(double porcentagemMestre) {
		this.porcentagemMestre = porcentagemMestre;
	}

	public double getPorcentagemEspecialista() {
		return porcentagemEspecialista;
	}

	public void setPorcentagemEspecialista(double porcentagemEspecialista) {
		this.porcentagemEspecialista = porcentagemEspecialista;
	}

}
